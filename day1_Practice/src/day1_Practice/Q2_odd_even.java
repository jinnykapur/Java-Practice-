package day1_Practice;

import java.util.*;

public class Q2_odd_even {
 public static void main(String[] args) {
	Scanner scn=new Scanner(System.in);
	
	int n=scn.nextInt();
	
	if(n%2==0) {
		System.out.println("The given number is EVEN.");
	}
	else {
		System.out.println("The given number is ODD.");
	}
	 
}
}
